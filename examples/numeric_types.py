def add_number(number_1: int, number_2: int) -> int:
    return  number_1 + number_2

def add_floats(float_1: float, float_2: float) -> float:
    secret_factor: float = 4.5
    return float_1 + float_2 + secret_factor

def show_variable_type(variable):
    return type(variable)

def convert_float_to_int(float_number: float) -> int:
    return int(float_number)
